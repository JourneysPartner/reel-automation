// 表示テキスト（字幕・フックに出す原文）→ VOICEVOX 合成用テキスト への変換ルール。
// 表示は漢字のまま美しく、音声だけ正しい読みになるようにする。
//
// 変換ルールを追加するときの指針:
// - 一般語の読みを破壊しないよう、必ず「文脈」を含めて置換する
//   悪い例: /人/g → /ひと/g  ← 「外国人」「3人」も壊れる
//   良い例: /([ぁ-ん])人/g → "$1ひと"  ← 「な人」「の人」「る人」だけが該当
// - 単独の漢字（人・代・代等）を不特定置換しない
// - 既存のテスト文に通して、誤変換が起きないか目視確認する

/** 表示テキストを音声合成用テキストに変換する。 */
export function toVoiceText(display) {
  if (!display) return display;
  let s = String(display);

  // --- 範囲を表す 波ダッシュ / 全角チルダ → 「から」 ---
  // 〜 (U+301C) と ～ (U+FF5E) はどちらも VOICEVOX の辞書に読みがなく、無音でスキップされる。
  // 「8〜15%」→「8から15%」と読むよう置換する。
  // ※ jpWrap の数式チェーンで表示側は「8〜15」を1アトム扱いのままなので、字幕の見た目は変わらない。
  s = s.replace(/[〜～]/g, "から");

  // --- 単位記号 ---
  // ㎡ ㎢ ㎤ ㎥ ㎜ ㎝ ㎞ ㎏ ㎎ など合字を VOICEVOX が読めない
  s = s.replace(/㎡/g, "平方メートル");
  s = s.replace(/㎢/g, "平方キロメートル");
  s = s.replace(/㎤/g, "立方センチメートル");
  s = s.replace(/㎥/g, "立方メートル");
  s = s.replace(/㎜/g, "ミリメートル");
  s = s.replace(/㎝/g, "センチメートル");
  s = s.replace(/㎞/g, "キロメートル");
  s = s.replace(/㎏/g, "キログラム");
  s = s.replace(/㎎/g, "ミリグラム");
  s = s.replace(/㎖/g, "ミリリットル");
  s = s.replace(/㎗/g, "デシリットル");

  // --- 「~人」を「~にん」と読ませる（税務・法務系の固有語で VOICEVOX が『じん』と誤読する対策） ---
  // 個人 (こじん)・法人 (ほうじん)・外国人 (がいこくじん) は正しく読まれるため対象外。
  // 訂正が確認された・または訂正見込みが高い『にん』読み複合語のみ列挙する。
  const NIN_COMPOUNDS = {
    "相続人": "そうぞくにん",
    "被相続人": "ひそうぞくにん",
    "代理人": "だいりにん",
    "保証人": "ほしょうにん",
    "連帯保証人": "れんたいほしょうにん",
    "差出人": "さしだしにん",
    "受取人": "うけとりにん",
    "借入人": "かりいれにん",
    "譲受人": "じょうじゅにん",
    "譲渡人": "じょうとにん",
    "名義人": "めいぎにん",
    "扶養人": "ふようにん",
    "納税義務人": "のうぜいぎむにん",
    "使用人": "しようにん",
    "管財人": "かんざいにん",
  };
  // 長い語から先に置換して部分マッチ（例: 相続人 が 被相続人 を分解しない）を防ぐ
  for (const w of Object.keys(NIN_COMPOUNDS).sort((a, b) => b.length - a.length)) {
    s = s.replace(new RegExp(w, "g"), NIN_COMPOUNDS[w]);
  }

  // --- 「人」を「ひと」と読ませる ---
  // ひらがなが直前にある「人」は「ひと」確定（「な人」「の人」「る人」「い人」「う人」等）
  // 「外国人」「3人」「日本人」「殺人」は前が漢字・数字なので影響しない
  s = s.replace(/([ぁ-んー])人/g, "$1ひと");

  // --- 「~代」を「~だい」と読ませる（VOICEVOX が「カワ」と読みがち） ---
  const DAI_PREFIXES = [
    "電気", "ガス", "水道", "光熱", "通信", "燃料", "ネット",
    "スマホ", "携帯", "電話", "家賃", "食事", "食費",
    "送料", "材料", "薬", "ガソリン", "駐車場", "駐車",
  ];
  for (const w of DAI_PREFIXES) {
    s = s.replace(new RegExp(w + "代", "g"), w + "だい");
  }

  // --- 「~部屋」を連濁で「~べや」と読ませる ---
  // 単独「部屋」は「へや」のまま。複合語のみ「べや」に倒す。
  const BEYA_PREFIXES = ["仕事", "子供", "子ども", "寝", "大", "小", "個", "客", "会議"];
  for (const w of BEYA_PREFIXES) {
    s = s.replace(new RegExp(w + "部屋", "g"), w + "べや");
  }

  // --- 「~手」（動詞連用形+手）を「~て」と読ませる ---
  // VOICEVOX は「打ち手」の「手」を音読み『シュ』で読みがち（打ち手→ウチシュ）
  // これらは全て「〜する人・〜する動作」の意で 手=て が正しい
  const TE_COMPOUNDS = [
    "打ち手", "決め手", "攻め手", "責め手", "守り手",
    "買い手", "売り手", "使い手", "書き手", "読み手",
    "話し手", "聞き手", "送り手", "受け手", "貸し手",
    "借り手", "作り手", "担い手", "指し手", "投げ手",
    "取り手", "選び手", "織り手", "詠み手", "弾き手",
    "書き手", "描き手", "歌い手", "踊り手",
  ];
  const TE_READINGS = {
    "打ち手": "うちて", "決め手": "きめて", "攻め手": "せめて",
    "責め手": "せめて", "守り手": "まもりて",
    "買い手": "かいて", "売り手": "うりて", "使い手": "つかいて",
    "書き手": "かきて", "読み手": "よみて", "話し手": "はなして",
    "聞き手": "ききて", "送り手": "おくりて", "受け手": "うけて",
    "貸し手": "かして", "借り手": "かりて", "作り手": "つくりて",
    "担い手": "にないて", "指し手": "さして", "投げ手": "なげて",
    "取り手": "とりて", "選び手": "えらびて", "織り手": "おりて",
    "詠み手": "よみて", "弾き手": "ひきて", "描き手": "かきて",
    "歌い手": "うたいて", "踊り手": "おどりて",
  };
  for (const w of new Set(TE_COMPOUNDS)) {
    s = s.replace(new RegExp(w, "g"), TE_READINGS[w]);
  }

  // --- 「~はね」を「~わね」に置換 ---
  // VOICEVOX は文中の「はね」を動詞「跳ね」と誤認識して『ハネ』と読みがち。
  // これを防ぐため、係助詞「は」+ 終助詞「ね」と判断できる文脈で 「はね」→「わね」に書き換える。
  // 「わね」なら VOICEVOX が「わ」「ね」と自然に連続して発音する（途中に間が入らない）。
  // 対象:
  //   - 直前が助詞: の / に / で / と / も / が / を / へ / は （例: 大事な「の」はね → 大事な「の」わね）
  //   - 直前が漢字   （例: 光熱費はね → 光熱費わね）
  //   - 直前がカタカナ（例: メリットはね → メリットわね）
  // 「跳ね」等の動詞は「は+ね」でなく「跳+ね」の並びのためこの規則にマッチしない（false positive なし）
  s = s.replace(/([のにでとものがをへは一-龯ァ-ヶー])はね/g, "$1わね");

  return s;
}

// ---------- CLI (確認用) ----------
// node src/voiceText.js
async function main() {
  const cases = [
    "フリーランスの人、夏の電気代って全部経費にしてない？",
    "具体的にはね、仕事部屋の面積÷家全体の面積で比率を出すんだよ。",
    "たとえば20㎡の部屋を60㎡の家で使ってたら、按分率は約33%になるんだよ。",
    "経費にしてる人いない？",
    "外国人や日本人は対象外。",
    "3人で月100万円。",
    "ガソリン代と駐車場代を経費に。",
    "子供部屋と寝部屋の電気代。",
  ];
  for (const c of cases) {
    const out = toVoiceText(c);
    console.log("元:", c);
    console.log("音:", out);
    console.log();
  }
}
if (import.meta.url === `file://${process.argv[1]}` || process.argv[1]?.endsWith("voiceText.js")) {
  main().catch((e) => { console.error(e); process.exit(1); });
}
